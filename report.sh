#!/bin/bash
# =============================================================================
# report.sh – Скрипт отправки анонимного отчёта об ошибке
# Версия: 1.0.0
# =============================================================================
# Режимы:
#   --auto <step> – автоматический сбор и отправка (без интерактива)
#   без параметров – интерактивный режим (запрашивает разрешение)
# =============================================================================

RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
NC='\033[0m'

VERSION="1.0.0"

# -----------------------------------------------------------------------------
# Сбор анонимного отчёта (удаляем IP, пароли, имена пользователей)
# -----------------------------------------------------------------------------
collect_report() {
    local step="$1"
    local log_file="/var/log/pbx/freepbx17-install-*.log"
    local latest_log=$(ls -t $log_file 2>/dev/null | head -1)
    if [ -z "$latest_log" ]; then
        echo "❌ Лог установки не найден"
        return 1
    fi
    # Берём последние 200 строк, удаляем IP-адреса, пароли, имена пользователей
    local safe_log=$(tail -200 "$latest_log" | sed -E 's/([0-9]{1,3}\.){3}[0-9]{1,3}/XXX.XXX.XXX.XXX/g' | sed 's/password=[^ ]*/password=XXX/g' | sed 's/IDENTIFIED BY '[^']*'/IDENTIFIED BY XXX/g')
    local report_data="Step: $step\nVersion: $(grep 'SCRIPT_VERSION=' russian.sh 2>/dev/null | cut -d'"' -f2)\nDate: $(date)\n\nLog:\n$safe_log"
    echo "$report_data"
    return 0
}

# -----------------------------------------------------------------------------
# Отправка отчёта (здесь можно реализовать отправку на GitHub Issue, Gist и т.п.)
# -----------------------------------------------------------------------------
send_report() {
    local report="$1"
    # Для простоты сохраняем локально и выводим инструкцию.
    # В реальном проекте можно добавить отправку через curl на ваш сервер или в приватный Gist.
    local report_file="/tmp/freepbx_report_$(date +%Y%m%d_%H%M%S).txt"
    echo "$report" > "$report_file"
    echo -e "${GREEN}✅ Отчёт сохранён: $report_file${NC}"
    echo "   Вы можете отправить этот файл разработчику вручную или через систему Issues GitHub."
    # Пример отправки через curl (закомментировано):
    # curl -X POST -H "Authorization: token YOUR_GITHUB_TOKEN" ... 
}

# -----------------------------------------------------------------------------
# Основная логика
# -----------------------------------------------------------------------------
if [ "$1" = "--auto" ] && [ -n "$2" ]; then
    # Автоматический режим – без запроса, сразу собираем и отправляем
    report_data=$(collect_report "$2")
    if [ $? -eq 0 ]; then
        send_report "$report_data"
    else
        echo "❌ Не удалось собрать отчёт"
        exit 1
    fi
else
    # Интерактивный режим – запрашиваем разрешение
    echo -e "${BLUE}════════════════════════════════════════════════════════════${NC}"
    echo -e "${GREEN}📝 Скрипт отправки анонимного отчёта об ошибке v$VERSION${NC}"
    echo -e "${BLUE}════════════════════════════════════════════════════════════${NC}"
    echo ""
    echo "❓ Отправить анонимный отчёт разработчику?"
    echo "   (будут удалены IP-адреса, пароли и личные данные)"
    read -p "   Отправить? (y/n): " -n 1 -r
    echo
    if [[ $REPLY =~ ^[Yy]$ ]]; then
        report_data=$(collect_report "manual")
        send_report "$report_data"
    else
        echo "ℹ️ Отчёт не отправлен."
    fi
fi
